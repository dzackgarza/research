"""Constructibility = derivability of canonical maps in a typed finite-limit graph.

Does **not** search for named nodes such as ``Rings.Graded.Finite``. Decides
whether the corresponding finite-limit expression is generated by the
normalized structural sketch.

Availability: classifier ``A`` with ``ℓ_A : T_A → H_A`` is available on ``D``
iff there is an admissible directed structural path ``D → H_A``
(not ``D → T_A``). Imposing ``A`` extends a formal cone; the checker never
constructs a categorical pullback object.

Governing doctrine: ``category-expressions-generate-projection-hierarchy``;
algorithm: 2026-07-21 finite-limit constructibility directive.
"""

from __future__ import annotations

from collections.abc import Iterable
from dataclasses import dataclass, field
from typing import Any

from .composed_identity import (
    ADDITIVE_AXIOM_TO_FLAT,
    ADDITIVE_BASE_TO_MAGMAS,
    CONSTRUCTION_TAGS,
    is_composed_cls_key,
    normalized_reading,
    parse_composed,
)
from .semantic_seed import SemanticSeed, load_semantic_seed

_STRUCTURAL_KINDS = frozenset(
    {
        "forgetful",
        "full_inclusion",
        "pullback_projection",
        "theorem_inclusion",
    }
)


@dataclass(frozen=True, slots=True)
class StructuralEdge:
    id: str
    source: str
    target: str
    role: str | None
    kind: str
    origin: str


@dataclass(frozen=True, slots=True)
class ClassifierRec:
    id: str
    host: str
    total: str
    leg_id: str
    name: str
    idempotent: bool = True


@dataclass(frozen=True, slots=True)
class PullbackCone:
    """Named pullback ``apex = L ×_over R`` with registered projections."""

    id: str
    apex: str
    left_obj: str  # L
    right_obj: str  # R
    over: str  # B
    left_proj: str  # apex → L (edge id)
    right_proj: str  # apex → R
    left_base: str  # L → B
    right_base: str  # R → B


@dataclass(frozen=True, slots=True)
class ClassifierRequest:
    classifier_id: str
    along: str | None = None
    occurrence_id: str | None = None


@dataclass(frozen=True, slots=True)
class MapTerm:
    """Map class from the formal apex to ``target`` along structural edges."""

    target: str
    edges: tuple[str, ...] = ()
    role: str | None = None

    def then(self, edge: StructuralEdge) -> MapTerm:
        role = edge.role if edge.role is not None else self.role
        # Forgetful composites into Sets are underlying-set routes; do not
        # retain an upstream operation role (that spuriously splits Finite).
        if edge.target == "cat.sets" or edge.role == "underlying_set":
            role = "underlying_set"
        return MapTerm(target=edge.target, edges=self.edges + (edge.id,), role=role)


@dataclass
class FormalCone:
    ports: dict[str, MapTerm] = field(default_factory=dict)


@dataclass(frozen=True, slots=True)
class ConstructibilityResult:
    status: str
    base_id: str | None
    certificate: dict[str, MapTerm]
    remaining: tuple[str, ...]
    reachable: tuple[str, ...]
    detail: str
    expression: str = ""

    @property
    def ok(self) -> bool:
        return self.status == "CONSTRUCTIBLE"


@dataclass(frozen=True, slots=True)
class Constructibility:
    """Back-compat summary for mapping_build / older tests."""

    ok: bool
    base_id: str | None
    classifier_ids: tuple[str, ...]
    expression: str
    detail: str
    host_paths: tuple[tuple[str, str], ...] = ()


def _infer_role(arrow: dict[str, Any]) -> str | None:
    """Deprecated fallback: scrape provenance/notes when authored ``role`` is absent.

    Authored ``role`` / PortId on the arrow always wins via ``_arrow_role``.
    """
    text = f"{arrow.get('provenance') or ''} {arrow.get('notes') or ''}".lower()
    if "multiplicative" in text or "nonzero mult" in text:
        return "multiplicative_operation"
    if "additive" in text and ("magma" in text or "operation" in text):
        return "additive_operation"
    if arrow.get("target") == "cat.sets":
        return "underlying_set"
    if "underlying_set" in text:
        return "underlying_set"
    return None


def _arrow_role(arrow: dict[str, Any]) -> str | None:
    """Prefer authored ``role`` (PortId); fall back to deprecated prose inference."""
    authored = arrow.get("role")
    if isinstance(authored, str) and authored.strip():
        return authored.strip()
    return _infer_role(arrow)


def build_sketch(seed: SemanticSeed | None = None) -> dict[str, Any]:
    seed = seed or load_semantic_seed()
    nodes = {e["id"] for e in seed.entities if e.get("kind") != "alias"}
    by_id: dict[str, StructuralEdge] = {}
    out_adj: dict[str, list[StructuralEdge]] = {}

    def add_edge(edge: StructuralEdge) -> None:
        if edge.id in by_id:
            return
        by_id[edge.id] = edge
        out_adj.setdefault(edge.source, []).append(edge)

    for a in seed.arrows:
        kind = a.get("kind")
        if kind == "classifier_leg":
            continue  # second cospan leg — not traversed for availability
        if kind == "equivalence" and not a.get("preferred"):
            continue
        if kind not in _STRUCTURAL_KINDS and kind != "equivalence":
            continue
        src, tgt = a.get("source"), a.get("target")
        if not src or not tgt:
            continue
        add_edge(
            StructuralEdge(
                id=str(a["id"]),
                source=str(src),
                target=str(tgt),
                role=_arrow_role(a),
                kind=str(kind),
                origin="seed_arrow",
            )
        )

    classifiers: dict[str, ClassifierRec] = {}
    for c in seed.classifiers:
        host, total, leg = c.get("host_id"), c.get("domain_id"), c.get("leg_arrow_id")
        if not host or not total or not leg:
            continue
        classifiers[c["id"]] = ClassifierRec(
            id=c["id"],
            host=str(host),
            total=str(total),
            leg_id=str(leg),
            name=str(c.get("name") or c["id"]),
        )

    arrow_index = {a["id"]: a for a in seed.arrows}
    cones: list[PullbackCone] = []
    for e in seed.entities:
        d = e.get("definition") or {}
        if d.get("operation") != "pullback":
            continue
        left_fun, right_clf, over = d.get("left"), d.get("right"), d.get("over")
        if not left_fun or not right_clf or not over:
            continue
        left_arr = arrow_index.get(str(left_fun))
        clf = classifiers.get(str(right_clf))
        if left_arr is None or clf is None:
            continue
        L, R, B = str(left_arr["source"]), clf.total, str(over)
        left_proj = f"gen.proj.{e['id']}.left"
        right_proj = f"gen.proj.{e['id']}.right"
        add_edge(StructuralEdge(left_proj, e["id"], L, None, "pullback_projection", "generated_projection"))
        add_edge(StructuralEdge(right_proj, e["id"], R, None, "pullback_projection", "generated_projection"))
        # Ensure left_base exists as structural edge
        if str(left_fun) not in by_id and left_arr.get("kind") != "classifier_leg":
            add_edge(
                StructuralEdge(
                    str(left_fun),
                    str(left_arr["source"]),
                    str(left_arr["target"]),
                    _arrow_role(left_arr),
                    str(left_arr.get("kind") or "forgetful"),
                    "seed_arrow",
                )
            )
        cones.append(
            PullbackCone(
                id=e["id"],
                apex=e["id"],
                left_obj=L,
                right_obj=R,
                over=B,
                left_proj=left_proj,
                right_proj=right_proj,
                left_base=str(left_fun),
                right_base=clf.leg_id,
            )
        )

    return {
        "nodes": nodes,
        "by_id": by_id,
        "out_adj": out_adj,
        "classifiers": classifiers,
        "cones": cones,
        "seed": seed,
        "arrow_index": arrow_index,
    }


def map_closure(
    cone: FormalCone,
    sketch: dict[str, Any],
) -> dict[str, list[MapTerm]]:
    """Saturate canonical maps under composition and named pullback lifts.

    Map terms are path-class representatives keyed by ``(target, role)``.
    Raw path enumeration is forbidden — without that collapse, pullback
    introduction plus projection edges diverge.
    """
    out_adj: dict[str, list[StructuralEdge]] = sketch["out_adj"]
    by_id: dict[str, StructuralEdge] = sketch["by_id"]
    cones: list[PullbackCone] = sketch["cones"]
    arrow_index: dict[str, Any] = sketch["arrow_index"]

    maps: dict[str, list[MapTerm]] = {}
    # Equal-length inequivalent paths for (target, role) → AMBIGUOUS at use sites.
    path_collisions: set[tuple[str, str | None]] = set()

    def add(term: MapTerm) -> bool:
        """Keep one shortest representative per ``(target, role)`` class.

        If an equal-length inequivalent path is seen, record a collision so
        ``check_requests`` can return AMBIGUOUS instead of silently collapsing.
        """
        bucket = maps.setdefault(term.target, [])
        for i, existing in enumerate(bucket):
            if existing.role != term.role:
                continue
            if term.edges == existing.edges:
                return False
            if len(term.edges) < len(existing.edges):
                bucket[i] = term
                return True
            if len(term.edges) > len(existing.edges):
                return False
            path_collisions.add((term.target, term.role))
            return False
        bucket.append(term)
        return True

    for term in cone.ports.values():
        add(term)

    changed = True
    while changed:
        changed = False
        # Structural composition
        for term in [m for ms in maps.values() for m in ms]:
            for edge in out_adj.get(term.target, ()):
                if add(term.then(edge)):
                    changed = True
        # Pullback introduction: X→L, X→R, composites to B agree ⇒ X→apex
        for pb in cones:
            left_maps = list(maps.get(pb.left_obj, ()))
            right_maps = list(maps.get(pb.right_obj, ()))
            if not left_maps or not right_maps:
                continue
            for f in left_maps:
                for g in right_maps:
                    if not _agree_to_base(f, pb.left_base, g, pb.right_base, by_id, arrow_index):
                        continue
                    lift_id = f"lift.{pb.id}"
                    if lift_id not in by_id:
                        by_id[lift_id] = StructuralEdge(
                            id=lift_id,
                            source="__formal__",
                            target=pb.apex,
                            role=None,
                            kind="pullback_lift",
                            origin="pullback_introduction",
                        )
                    # Role of the lift follows the left factor (cospan left leg).
                    if add(MapTerm(pb.apex, f.edges + (lift_id,), f.role)):
                        changed = True
    # Stash collisions for check_requests (same sketch dict is reused).
    sketch["_path_collisions"] = path_collisions
    return maps


def _compose_edge(
    term: MapTerm,
    edge_id: str,
    by_id: dict[str, StructuralEdge],
    arrow_index: dict[str, Any],
) -> MapTerm | None:
    """Extend ``term`` by a registered edge id (structural or classifier leg)."""
    edge = by_id.get(edge_id)
    if edge is not None:
        if edge.source != term.target:
            return None
        return term.then(edge)
    arrow = arrow_index.get(edge_id)
    if arrow is None:
        return None
    if arrow.get("source") != term.target:
        return None
    role = term.role
    authored_or_inferred = _arrow_role(arrow)
    if authored_or_inferred is not None:
        role = authored_or_inferred
    return MapTerm(
        target=str(arrow["target"]),
        edges=term.edges + (edge_id,),
        role=role,
    )


def _agree_to_base(
    f: MapTerm,
    left_base_id: str,
    g: MapTerm,
    right_base_id: str,
    by_id: dict[str, StructuralEdge],
    arrow_index: dict[str, Any],
) -> bool:
    """True when ``f;left_base`` and ``g;right_base`` share a common target.

    Full path-equation coherence is not yet an e-graph; for registered named
    cones, agreeing endpoints of the two base composites is the check. Roles
    that both carry an ``underlying_set`` (or both lack a role) are treated as
    coherent; conflicting operation roles refuse the lift.
    """
    f_b = _compose_edge(f, left_base_id, by_id, arrow_index)
    g_b = _compose_edge(g, right_base_id, by_id, arrow_index)
    if f_b is None or g_b is None:
        return False
    if f_b.target != g_b.target:
        return False
    if f_b.role is not None and g_b.role is not None and f_b.role != g_b.role and "operation" in f_b.role and "operation" in g_b.role:
        # Distinct operation ports whose composites land on the same node are
        # not automatically coherent (e.g. additive vs multiplicative to Magmas).
        return False
    return True


def check_requests(
    base_id: str,
    requests: Iterable[ClassifierRequest],
    *,
    sketch: dict[str, Any] | None = None,
) -> ConstructibilityResult:
    sketch = sketch or build_sketch()
    classifiers: dict[str, ClassifierRec] = sketch["classifiers"]
    remaining = {(r.occurrence_id or r.classifier_id): r for r in requests}
    certificate: dict[str, MapTerm] = {}
    cone = FormalCone(ports={"base": MapTerm(base_id)})

    for _ in range(len(remaining) + 3):
        if not remaining:
            break
        maps = map_closure(cone, sketch)
        enabled: list[tuple[str, ClassifierRequest, MapTerm]] = []
        for occ_id, req in remaining.items():
            clf = classifiers.get(req.classifier_id)
            if clf is None:
                return ConstructibilityResult(
                    "NOT_CONSTRUCTIBLE",
                    base_id,
                    certificate,
                    tuple(remaining),
                    tuple(sorted(maps)),
                    f"unknown classifier {req.classifier_id}",
                )
            host_maps = list(maps.get(clf.host, ()))
            if req.along is not None:
                if req.along == "additive_presentation":
                    # Additive role: drop multiplicative Magmas ports.
                    host_maps = [m for m in host_maps if m.role != "multiplicative_operation"]
                elif req.along == "underlying_set":
                    host_maps = [m for m in host_maps if m.role in (None, "underlying_set") or m.target == "cat.sets"]
                elif req.along == "additive_operation":
                    host_maps = [m for m in host_maps if m.role in (None, "additive_operation")]
                elif req.along == "multiplicative_operation":
                    host_maps = [m for m in host_maps if m.role in (None, "multiplicative_operation")]
                else:
                    host_maps = [m for m in host_maps if m.role == req.along]
            else:
                roles = {m.role for m in host_maps}
                if len(roles) > 1 and any(r is not None for r in roles):
                    return ConstructibilityResult(
                        "AMBIGUOUS",
                        base_id,
                        certificate,
                        tuple(remaining),
                        tuple(sorted(maps)),
                        (f"ambiguous structural routes to {clf.host} for {req.classifier_id}; specify along= (roles={sorted(map(str, roles))})"),
                    )
            if not host_maps:
                continue
            # Path-class collisions matter when the role was not disambiguated.
            # With an explicit `along=`, role selection is enough until full
            # path-equation certificates exist.
            if req.along is None:
                collisions: set[tuple[str, str | None]] = sketch.get("_path_collisions", set())
                if any((m.target, m.role) in collisions for m in host_maps):
                    return ConstructibilityResult(
                        "AMBIGUOUS",
                        base_id,
                        certificate,
                        tuple(remaining),
                        tuple(sorted(maps)),
                        (f"ambiguous path classes to {clf.host} for {req.classifier_id}; need path-equation certificate or along="),
                    )
            support = min(host_maps, key=lambda m: len(m.edges))
            enabled.append((occ_id, req, support))

        if not enabled:
            break
        for occ_id, req, support in enabled:
            clf = classifiers[req.classifier_id]
            # Add classifier-total port (formal pullback); keep old ports (monotone)
            cone.ports[f"clf:{occ_id}"] = MapTerm(
                clf.total,
                support.edges + (f"impose.{clf.id}",),
                support.role,
            )
            certificate[occ_id] = support
            del remaining[occ_id]

    maps = map_closure(cone, sketch)
    if not remaining:
        return ConstructibilityResult(
            "CONSTRUCTIBLE",
            base_id,
            certificate,
            (),
            tuple(sorted(maps)),
            "all requested classifier occurrences discharged",
        )
    missing = []
    for occ_id, req in remaining.items():
        clf = classifiers.get(req.classifier_id)
        missing.append(f"{req.classifier_id}→{clf.host if clf else '?'}")
    return ConstructibilityResult(
        "NOT_CONSTRUCTIBLE",
        base_id,
        certificate,
        tuple(remaining),
        tuple(sorted(maps)),
        "no support map D→H_A for: " + ", ".join(missing),
    )


def _resolve_named_category(
    name: str,
    *,
    sketch: dict[str, Any],
    sage_to_entity: dict[str, str],
) -> str | None:
    if name in sage_to_entity:
        return sage_to_entity[name]
    seed: SemanticSeed = sketch["seed"]
    for e in seed.entities:
        if e.get("kind") == "alias":
            continue
        eid = e["id"]
        assert isinstance(eid, str)
        if eid == name or e.get("canonical_name") == name:
            return eid
        if name in (e.get("aliases") or []):
            return eid
    return None


def requests_from_composed_cls_key(
    cls_key: str,
    *,
    sketch: dict[str, Any] | None = None,
    sage_to_entity: dict[str, str] | None = None,
) -> tuple[str | None, list[ClassifierRequest], str]:
    sketch = sketch or build_sketch()
    sage_to_entity = sage_to_entity or {}
    classifiers: dict[str, ClassifierRec] = sketch["classifiers"]
    expression = normalized_reading(cls_key)
    if not is_composed_cls_key(cls_key):
        return None, [], expression
    sage_base, steps = parse_composed(cls_key)
    if not steps or steps[-1] in CONSTRUCTION_TAGS:
        return None, [], expression

    flat_steps = [ADDITIVE_AXIOM_TO_FLAT.get(s, s) for s in steps]
    base_id: str | None
    additive_tower = False

    # AdditiveMagmas.* / AdditiveSemigroups.* → Magmas + Additive + flat laws
    # (one-tower: never host on cat.additive_magmas with a duplicate classifier family).
    if sage_base in ADDITIVE_BASE_TO_MAGMAS:
        path = ADDITIVE_BASE_TO_MAGMAS[sage_base]
        prefix = path.split(".")[1:]  # Additive[.Associative…]
        flat_steps = [*prefix, *flat_steps]
        base_id = "cat.magmas"
        additive_tower = True
    else:
        base_id = _resolve_named_category(sage_base, sketch=sketch, sage_to_entity=sage_to_entity)

    if base_id is None:
        return None, [], expression

    reqs: list[ClassifierRequest] = []
    for i, name in enumerate(flat_steps):
        cands = [c for c in classifiers.values() if c.name == name]
        if not cands:
            return base_id, [], expression
        # Prefer Magmas/Sets classifiers; never a deleted additive_magmas duplicate family.
        prefer_host = base_id
        if name == "Additive" or additive_tower:
            prefer_host = "cat.magmas"
        ranked = sorted(
            cands,
            key=lambda c: (
                0 if c.host == prefer_host else 1,
                0 if c.id.startswith("clf.magmas") or c.id.startswith("clf.sets") else 1,
                0 if "additive_magmas" not in c.id else 2,
                1 if "catobject" in c.id or c.id.startswith("clf.ax_") else 0,
                c.id,
            ),
        )
        pick = ranked[0]
        along = None
        if pick.host == "cat.magmas" and base_id == "cat.rings" and name == "Commutative":
            along = "multiplicative_operation"
        elif additive_tower and pick.host == "cat.magmas" and name != "Additive":
            along = "additive_operation"
        reqs.append(ClassifierRequest(pick.id, along=along, occurrence_id=f"{pick.id}#{i}"))
    return base_id, reqs, expression


def check_constructibility(
    cls_key: str,
    *,
    seed: SemanticSeed | None = None,
    sage_to_entity: dict[str, str] | None = None,
) -> Constructibility:
    sketch = build_sketch(seed)
    base_id, reqs, expression = requests_from_composed_cls_key(cls_key, sketch=sketch, sage_to_entity=sage_to_entity)
    if base_id is None or not reqs:
        return Constructibility(
            False,
            base_id,
            (),
            expression,
            "could not parse base/classifiers for constructibility",
        )
    result = check_requests(base_id, reqs, sketch=sketch)
    return Constructibility(
        ok=result.ok,
        base_id=result.base_id,
        classifier_ids=tuple(r.classifier_id for r in reqs),
        expression=expression,
        detail=result.detail if result.ok else f"{result.status}: {result.detail}",
        host_paths=tuple((k, v.target) for k, v in result.certificate.items()),
    )


def constructibility_record(result: Constructibility) -> dict[str, Any]:
    return {
        "kind": "finite_limit_constructibility",
        "ok": result.ok,
        "base": result.base_id,
        "classifiers": list(result.classifier_ids),
        "expression": result.expression,
        "detail": result.detail,
        "certificate": [{"occurrence": a, "support_host": b} for a, b in result.host_paths],
    }


def resolve_base_entity(
    sage_base: str,
    *,
    seed: SemanticSeed,
    sage_to_entity: dict[str, str] | None = None,
) -> str | None:
    sketch = build_sketch(seed)
    return _resolve_named_category(sage_base, sketch=sketch, sage_to_entity=sage_to_entity or {})
