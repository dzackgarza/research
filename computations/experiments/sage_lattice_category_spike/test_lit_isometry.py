r"""Literature-cited isometry tests for the owned synthetic lattice spike (fast).

These cover the O(L) / reflection surface that ``test_literature_citations.py``
does NOT already pin: root reflections, the structure description and conjugacy
data of a small O(L), and the automorphism group of a direct sum (the gluing /
wreath-product order). The small-rank irreducible ``isometry_group().order()``
values (A1, A2, A3, D4) and the ``is_isometric`` True/False cases already live in
``test_literature_citations.py``; the slow rank >= 7 orders (E7, E8, A8, D8) live
in ``test_lit_isometry_ci.py``.

Every expected value is either quoted from a Zotero-held source or is the
GAP/Sage engine's own answer cross-checked against a published invariant; never
from memory. Each test also asserts the cheap characterizing invariants (rank,
signature pair, determinant, is_even, is_unimodular) alongside the significant
content, per the spike's test contract.

Sources (Zotero item key | Better-BibTeX key | markdown-extraction attachment):

  - Conway & Sloane, *Sphere Packings, Lattices and Groups*, 3rd ed. (1999).
    T2WVLTDB | CS10 | TCJKXU3D. Ch. 4 sec 4.1 (the automorphism/isometry group
    of a lattice); the hexagonal lattice A_2 has "a dihedral group of order 12";
    the gluing description g = g_0 g_1 g_2 with g_0 the Weyl group and G_1 the
    Coxeter-diagram graph automorphisms.
  - J. E. Humphreys, *Reflection Groups and Coxeter Groups* (1990). EM8H6HGI.
    Sec 1.1: the orthogonal reflection s_v(x) = x - 2(x,v)/(v,v) * v fixes the
    hyperplane v^perp pointwise and sends v to -v (an involution in O(V)).
"""

from __future__ import annotations

from math import factorial

import sage_lattice_category_spike.lattice_categories as lc


def test_root_reflection_is_the_humphreys_involution_fixing_the_mirror():
    r"""Humphreys, *Reflection Groups and Coxeter Groups*, sec 1.1 [EM8H6HGI]:
    for an anisotropic vector v the orthogonal reflection

        s_v(x) = x - (2 b(x, v) / q(v)) * v

    is the involution of O(L) that sends v to -v and fixes the mirror v^perp
    pointwise. For a root v (q(v) = 2) of a root lattice these are exactly the
    Weyl-group generators (CS10 Ch. 4: G_0 is generated by the reflections in the
    minimal vectors). The test checks all four defining properties on genuine
    roots of A_3 and D_4: s_v(v) = -v, s_v fixes every basis root orthogonal to
    v, s_v is an involution (order 2), and s_v preserves the form (lies in O(L),
    i.e. A^T G A = G).
    """
    for name, cheap in (("A3", ((3, 0), 4)), ("D4", ((4, 0), 4))):
        lattice = lc.Lattice(name)
        signature_pair, determinant = cheap
        # cheap characterizing invariants
        assert lattice.rank() == int(name[1:])
        assert lattice.signature_pair() == signature_pair
        assert lattice.determinant() == determinant
        assert lattice.is_even()
        assert not lattice.is_unimodular()

        group = lattice.isometry_group()
        for simple_root in lattice.basis():
            assert lattice.q(simple_root) == 2                 # v is a root
            s = lattice.reflection(simple_root)

            # s_v(v) = -v
            assert s(simple_root).coefficient_vector() == (-simple_root).coefficient_vector()
            # s_v fixes the mirror v^perp pointwise. u_i = 2 e_i - b(e_i,v) v lies in
            # v^perp (b(u_i,v) = 2 b(e_i,v) - b(e_i,v) q(v) = 0 since q(v) = 2); these
            # span the rank-1 mirror, and every one must be fixed.
            v_coeffs = simple_root.coefficient_vector()
            mirror = []
            for e in lattice.basis():
                u = lattice(2 * e.coefficient_vector() - lattice.b(e, simple_root) * v_coeffs)
                if u.coefficient_vector() != 0 * v_coeffs:
                    mirror.append(u)
            assert len(mirror) == lattice.rank() - 1           # a rank-(n-1) mirror
            for u in mirror:
                assert lattice.b(u, simple_root) == 0          # u really is in v^perp
                assert s(u).coefficient_vector() == u.coefficient_vector()
            # involution: s^2 = id and s != id
            assert not s.is_identity()
            assert (s * s).is_identity()
            # form-preserving: an element of O(L)
            assert s.is_isometry()
            assert s in group


def test_o_a2_is_the_order_12_dihedral_group_of_the_hexagon():
    r"""CS10 Ch. 4 sec 4.1 [CS10]: "the automorphism group of the hexagonal
    lattice ... is a dihedral group of order 12". The hexagonal lattice is A_2,
    so O(A_2) is the dihedral group of order 12 (= the symmetry group of a regular
    hexagon). GAP's structure description of that group is "D6" (dihedral, 6-gon),
    and a dihedral group of order 12 has 6 conjugacy classes; the count is the
    GAP/Sage engine's own answer, cross-checked here against the published group.
    Each returned conjugacy-class representative is verified to be a genuine
    isometry of A_2.
    """
    a2 = lc.Lattice("A2")
    # cheap characterizing invariants
    assert a2.rank() == 2
    assert a2.signature_pair() == (2, 0)
    assert a2.determinant() == 3
    assert a2.is_even()
    assert not a2.is_unimodular()

    group = a2.isometry_group()
    assert group.is_finite()
    assert group.order() == 12                                 # CS10: dihedral, order 12
    assert group.structure_description() == "D6"               # GAP's name for the order-12 dihedral group

    representatives = group.conjugacy_classes_representatives()
    assert len(representatives) == 6                            # dihedral order 12 has 6 conjugacy classes
    for representative in representatives:
        assert representative in group                          # each rep is a genuine isometry
        assert representative.is_isometry()
    # representatives are pairwise distinct as matrices
    assert len({representative.matrix() for representative in representatives}) == 6


def test_orthogonal_group_of_a2_plus_a2_is_the_wreath_product():
    r"""CS10 Ch. 4 gluing theory [CS10]: for L a direct sum of root lattices the
    automorphism group has order g = g_0 g_1 g_2, where g_0 is the product of the
    component Weyl groups, G_1 is the Coxeter-diagram graph-automorphism group, and
    g_2 permutes isometric components. For A_2 (+) A_2 with no glue this is the
    wreath product O(A_2) wr S_2:

        |O(A_2 (+) A_2)| = |O(A_2)|^2 * 2!  =  12^2 * 2  =  288,

    the component-permuting isometries that the irreducible-root-lattice orders
    (already pinned in test_literature_citations.py) do not exercise. |O(A_2)| = 12
    is CS10's order-12 dihedral hexagon group.
    """
    a2 = lc.Lattice("A2")
    lattice = a2.direct_sum(a2)
    # cheap characterizing invariants
    assert lattice.rank() == 4
    assert lattice.signature_pair() == (4, 0)
    assert lattice.determinant() == 9                          # det(A2)^2 = 3^2
    assert lattice.is_even()
    assert not lattice.is_unimodular()

    order_a2 = a2.isometry_group().order()
    assert order_a2 == 12
    group = lattice.isometry_group()
    assert group.is_finite()
    assert group.order() == order_a2**2 * factorial(2)         # wreath product O(A2) wr S2 = 288
    assert group.order() == 288
